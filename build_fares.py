def main():
    companies = {}
    for name, cfg in COMPANIES.items():
        if cfg.get('ic_fare') is None:
            continue  # 未実装(JR西日本はrouterがJR東海テーブルへ委譲...せずfallback)
        ent = {}
        if '_doc' in cfg:
            ent['_doc'] = cfg['_doc']
        if cfg.get('match'):
            ent['match'] = cfg['match']
        if cfg.get('prefix'):
            ent['prefix'] = cfg['prefix']
        if not ent.get('match') and not ent.get('prefix'):
            ent['match'] = [name]
        ent['ic_fare'] = cfg['ic_fare']
        companies[name] = ent

    out = {
        '_doc': '会社別IC運賃テーブル(距離km→円)。build_fares.py で生成。'
                'match=正規化後exact一致(優先), prefix=最長前方一致。'
                'JR東は2026-03-14改定(電車特定区間廃止)、西武2026-03、TX2026-03、京急2023-10等 各社現行',
        'companies': companies,
        'surcharges': SURCHARGES,
        'default_fare_per_km': 25,
    }
    with open('fares.json', 'w') as f:
        json.dump(out, f, ensure_ascii=False, separators=(',', ':'))
    n_bands = sum(len(c['ic_fare']) for c in companies.values())
    print(f'companies: {len(companies)}, total bands: {n_bands}')


if __name__ == '__main__':
    main()
